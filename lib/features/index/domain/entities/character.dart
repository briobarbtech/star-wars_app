abstract class Character {
  Character({
    required this.birthYear,
    required this.eyeColor,
    required this.gender,
    required this.hairColor,
    required this.skinColor,
    required this.height,
    required this.homeworld,
    required this.mass,
    required this.name,
  });

  String birthYear;
  String eyeColor;
  String gender;
  String hairColor;
  String skinColor;
  String height;
  String homeworld;
  String mass;
  String name;
}
